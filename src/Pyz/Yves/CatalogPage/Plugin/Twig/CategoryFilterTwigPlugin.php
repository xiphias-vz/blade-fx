namespace Pyz\Yves\CatalogPage\Plugin\Twig;

use Pyz\Yves\CatalogPage\Twig\CategoryFilterTwigExtension;
use Spryker\Service\Container\ContainerInterface;
use Spryker\Shared\TwigExtension\Dependency\Plugin\TwigPluginInterface;
use Spryker\Yves\Kernel\AbstractPlugin;
use Twig\Environment;

/**
 * @method \Pyz\Yves\CatalogPage\CatalogPageFactory getFactory()
 */
class CategoryFilterTwigPlugin extends AbstractPlugin implements TwigPluginInterface
{
    /**
     * {@inheritDoc}
     *
     * @api
     *
     * @param \Twig\Environment $twig
     * @param \Spryker\Service\Container\ContainerInterface $container
     *
     * @return \Twig\Environment
     */
    public function extend(Environment $twig, ContainerInterface $container): Environment
    {
        $twig->addExtension(new CategoryFilterTwigExtension());

        return $twig;
    }
}
